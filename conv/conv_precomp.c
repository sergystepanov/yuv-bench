// A precomputed RGBA to YUV converter.
void rgbaToYuvLut(void *destination, void *source, int width, int height, chromaPos chroma) {
    const int image_size = width * height;
    unsigned char *rgba = source;
    unsigned char *dst_y = destination;
    unsigned char *dst_u = dst_y + image_size;
    unsigned char *dst_v = dst_y + image_size + image_size / 4;

    // Y plane
    for (int i = 0, len = image_size; i < len; i += 4 * 4) {
        *dst_y++ = (lut66[rgba[i]] + lut129[rgba[i + 1]] + lut25[rgba[i + 2]]) + 16;
        *dst_y++ = (lut66[rgba[i + 4]] + lut129[rgba[i + 4 + 1]] + lut25[rgba[i + 4 + 2]]) + 16;
        *dst_y++ = (lut66[rgba[i + 8]] + lut129[rgba[i + 8 + 1]] + lut25[rgba[i + 8 + 2]]) + 16;
        *dst_y++ = (lut66[rgba[i + 12]] + lut129[rgba[i + 12 + 1]] + lut25[rgba[i + 12 + 2]]) + 16;
    }

    int r1, g1, b1, stride;
    // U+V plane
    if (chroma == TOP_LEFT) {
        for (int y = 0; y < height; y += 2) {
            stride = 4 * y * width;
            for (int x = 0; x < width; x += 2) {
                r1 = 4 * x + stride;
                g1 = r1 + 1;
                b1 = g1 + 1;
                *dst_u++ = lut_m_38[rgba[r1]] + lut_m_74[rgba[g1]] + lut112[rgba[b1]] + 128;
                *dst_v++ = lut112[rgba[r1]] + lut_m_94[rgba[g1]] + lut_m_18[rgba[b1]] + 128;
            }
        }
    } else if (chroma == BETWEEN_FOUR) {
        int r2, g2, b2, r3, g3, b3, r4, g4, b4;

        for (int y = 0; y < height; y += 2) {
            stride = 4 * y * width;
            for (int x = 0; x < width; x += 2) {
                // (1 2) x x
                //  x x  x x
                r1 = 4 * x + stride;
                g1 = r1 + 1;
                b1 = g1 + 1;
                r2 = r1 + 4;
                g2 = r2 + 1;
                b2 = g2 + 1;
                //  x x  x x
                // (3 4) x x
                r3 = r1 + 4 * width;

                g3 = r3 + 1;
                b3 = g3 + 1;
                r4 = r3 + 4;
                g4 = r4 + 1;
                b4 = g4 + 1;
                *dst_u++ = (lut_m_38[rgba[r1]] + lut_m_74[rgba[g1]] + lut112[rgba[b1]] +
                            lut_m_38[rgba[r2]] + lut_m_74[rgba[g2]] + lut112[rgba[b2]] +
                            lut_m_38[rgba[r3]] + lut_m_74[rgba[g3]] + lut112[rgba[b3]] +
                            lut_m_38[rgba[r4]] + lut_m_74[rgba[g4]] + lut112[rgba[b4]] + 512) >> 2;
                *dst_v++ = (lut112[rgba[r1]] + lut_m_94[rgba[g1]] + lut_m_18[rgba[b1]] +
                            lut112[rgba[r2]] + lut_m_94[rgba[g2]] + lut_m_18[rgba[b2]] +
                            lut112[rgba[r3]] + lut_m_94[rgba[g3]] + lut_m_18[rgba[b3]] +
                            lut112[rgba[r4]] + lut_m_94[rgba[g4]] + lut_m_18[rgba[b4]] + 512) >> 2;
            }
        }
    }
}
